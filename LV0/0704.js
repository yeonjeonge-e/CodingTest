// 두 수의 합

/* 
< 문제설명 >
- 정수 num1과 num2가 주어질 때,
  num1과 num2의 합을 return하도록 soltuion 함수를 완성해주세요.

- 제한사항
  -50000 ≤ num1 ≤ 50000
  -50000 ≤ num2 ≤ 50000  */

function solution(num1, num2) {
  return num1 + num2;
}



